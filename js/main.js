function down() {
    var doc = document.getElementById('drop');

    if (doc.style.display ==  'block')
    {
        doc.style.display =  'none';
    }
    else
    {
        doc.style.display =  'block';   
    } 
}
function up() {
    var doc = document.getElementById('drop');

    if (doc.style.display ==  'block')
    {
        doc.style.display =  'none';
    }
}